from sys import stdin


input = stdin.readline
MOD = 1234567


def main():
    # ì²˜ìŒì—” ë©”ëª¨ì´ì œì´ì…˜+DFSë¡œ í’€ë ¤ê³  í–ˆìœ¼ë‚˜... DPë¡œ í’€ë©´ í›¨ì”¬ ê°„ë‹¨í•¨.
    # ì–´ì°¨í”¼ ê²½ë¡œ/ê·œì¹™ì´ ëª…í™•í•˜ê²Œ ì •í•´ì ¸ìˆê¸° ë•Œë¬¸ì„.
    numbers = [[7], [2, 4], [1, 3, 5], [2, 6], [1, 5, 7], [2, 4, 6, 8],
               [3, 5, 9], [4, 8, 0], [5, 7, 9], [6, 8]]
    
    # íš¨ìœ¨ì„ ìœ„í•´ dp[num][ê¸¸ì´] í˜•íƒœë¡œ ì €ì¥í•¨.
    # dp[num][i]: ê¸¸ì´ê°€ iê³  numìœ¼ë¡œ ëë‚˜ëŠ” ê²½ìš°ì˜ ìˆ˜
    dp = [[0] * 1001 for _ in range(10)]

    # ê¸¸ì´ê°€ 1ì¼ë•Œì—ëŠ” ëª¨ë‘ 1ê°œì”©ë§Œ ê°€ëŠ¥
    for i in range(10):
        dp[i][1] = 1
    
    # ë°˜ë³µë¬¸ ëŒë¦´ë•Œì—ëŠ” i -> num ìˆœì„œë¡œ ê°€ì•¼ ì•ˆì „í•¨.
    # ì´ì „ ê¸¸ì´ì˜ ê°’(i-1)ì„ ì°¸ì¡°í•˜ê¸° ë•Œë¬¸.
    for i in range(2, 1001):
        for num in range(10):
            for j in numbers[num]:
                # ğŸš¨dp[num][i] += dp[j][i-1] % MOD ë¡œ í•˜ë©´ dp[j][i-1]ì—ë§Œ MODë¥¼ ì·¨í•˜ê²Œ ë¨.
                dp[num][i] = (dp[num][i] + dp[j][i-1]) % MOD
    
    T = int(input())

    for _ in range(T):
        N = int(input())
        total = sum(dp[i][N] for i in range(10)) % MOD

        print(total)


main()