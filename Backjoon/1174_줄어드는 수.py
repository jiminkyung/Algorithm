def main():
    N = int(input())
    nums = []

    def dfs(num: int):
        nonlocal nums

        # numì„ ë¦¬ìŠ¤íŠ¸ì— ì¶”ê°€ í›„, ë¦¬ìŠ¤íŠ¸ì˜ ê¸¸ì´ê°€ Nì´ ëœë‹¤ë©´ return
        nums.append(num)
