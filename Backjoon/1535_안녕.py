from sys import stdin


input = stdin.readline

def main():
    N = int(input())
    strength = list(map(int, input().split()))
    pleasure = list(map(int, input().split()))

    # dp[a][b]: aë²ˆì§¸ ì‚¬ëžŒê¹Œì§€ ì„ íƒ, ì²´ë ¥ì´ bì¼ë•Œì˜ ìµœëŒ€ ê¸°ì¨ ìˆ˜ì¹˜
        # ðŸš¨-1ë¡œ ì´ˆê¸°í™”í•˜ëŠ”ê²Œ ë‚˜ìŒ. 0ìœ¼ë¡œ ì´ˆê¸°í™” ì‹œ ê°€ëŠ¥í•œ ê²½ìš°ê°€ ì•„ë‹˜ì—ë„ ì²˜ë¦¬í•´ë²„ë¦¼.
        # ex) ì²«ë²ˆì§¸ ì‚¬ëžŒì˜ ì²´ë ¥, ê¸°ì¨ì´ (60, 10)ì´ê³  ë‘ë²ˆì§¸ ì‚¬ëžŒì˜ ì²´ë ¥, ê¸°ì¨ì´ (20, 15)ì¼ê²½ìš°,
        # dp[0][30] = 0 (ì²´ë ¥ 30 ë‚¨ìŒ, ê¸°ì¨ 0)ì€ ê°€ëŠ¥í•œ ìƒíƒœê°€ ì•„ë‹˜. í•˜ì§€ë§Œ ë‘ë²ˆì§¸ ì‚¬ëžŒ ì²˜ë¦¬ ì‹œ ê°€ëŠ¥í•˜ë‹¤ê³  íŒë‹¨í•´ë²„ë¦¼.
        # => dp[1][10] = 15 ë¼ëŠ” ìž˜ëª»ëœ ê°’ì´ ì €ìž¥ë˜ì–´ë²„ë¦¼.
        # => ì–´ì°¨í”¼ ì •ë‹µì€ ì œëŒ€ë¡œ ì²˜ë¦¬ëœ ê°’ì´ ì„ íƒë˜ê² ì§€ë§Œ, ì“¸ë°ì—†ëŠ” ì“°ë ˆê¸°ê°’ë“¤ì´ ì €ìž¥ëœë‹¤.
    dp = [[-1] * 101 for _ in range(N)]
    # ì²«ë²ˆì§¸ ì‚¬ëžŒì˜ ê²½ìš° ë¯¸ë¦¬ ì²˜ë¦¬
    dp[0][100] = 0
    if 100 - strength[0] > 0:
        dp[0][100 - strength[0]] = pleasure[0]

    for i in range(1, N):
        s, p = strength[i], pleasure[i]
        for j in range(101):
            if dp[i - 1][j] != -1:
                # ië²ˆì§¸ ì‚¬ëžŒ ì„ íƒ X
                # (i, j) ì¡°í•©ì€ ì´ ë•Œ ì²˜ìŒ ë“±ìž¥í•˜ëŠ”ê²ƒì´ë¯€ë¡œ, max ì—†ì´ ë°”ë¡œ ì´ì „ê°’ì„ ê°€ì ¸ì™€ë„ ëœë‹¤.
                dp[i][j] = dp[i - 1][j]

                # ië²ˆì§¸ ì‚¬ëžŒ ì„ íƒ O
                # ì´ ê²½ìš°, j-sëŠ” ì´ì „ì˜ jì˜€ì„ ê°€ëŠ¥ì„±ì´ ìžˆìœ¼ë¯€ë¡œ maxë¡œ ë¹„êµí•´ì¤˜ì•¼í•¨.
                if j - s > 0:
                    dp[i][j - s] = max(dp[i][j - s], dp[i - 1][j] + p)

    print(max(dp[N - 1]))


main()