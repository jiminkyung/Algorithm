from sys import stdin


input = stdin.readline

def main():
    N = int(input())
    
    if N == 1:
        print(1)
        return

    left = right = 1
    curr = 1
    cnt = 0

    # μ¤λ¥Έμ½ ν¬μΈν„°κ°€ Nμ μ λ°κ°’ μ΄ν•μΌλ•κΉμ§€λ§
    while right <= (N+1) // 2:
        # ν„μ¬ sumμ΄ Nλ³΄λ‹¤ μ‘λ‹¤λ©΄ μ¤λ¥Έμ½ ν¬μΈν„° μ΄λ™, curr μ¦κ°€
        if curr < N:
            right += 1
            curr += right
        # Nλ³΄λ‹¤ ν¬λ‹¤λ©΄ curr κ°μ† ν›„ μ™Όμ½ ν¬μΈν„° μ΄λ™
        elif curr > N:
            curr -= left
            left += 1
        # Nμ΄λΌλ©΄ μΉ΄μ΄νΈ ν›„ curr κ°μ†, μ™Όμ½ ν¬μΈν„° μ΄λ™
        else:
            cnt += 1
            curr -= left
            left += 1
    
    # μκΈ° μμ‹ (N)λ„ κ²½μ°μ μμ— ν¬ν•¨λλ―€λ΅ +1 μ²λ¦¬ (λ‹¨, N = 1μΌκ²½μ°μ—” X)
    print(cnt + 1)


main()


